#include <REGX52.H>
/**
	*@brief  定时器1初始化，100微秒@12.000MHz
	*@param  无
	*@retval  无
*/
void Timer1_Init()		
{
	TMOD &= 0x0F;		//设置定时器模式
	TMOD |= 0x10;		//设置定时器模式
	TL1 = 0x9C;				//设置定时初始值
	TH1 = 0xFF;				//设置定时初始值
	TF1 = 0;			//清除TF1标志
	TR1 = 1;			//定时器1开始计时
	ET1 = 1;			//使能定时器1中断
	EA=1;               //全局中断使能标志(1：全局中断使能，0：全局中断失能)
	PT0=0;             //低优先级中断
}